# Index by state for quick lookup
_BY_STATE = {}
for _loc in US_LOCATIONS:
    _state = _loc[2]
    if _state not in _BY_STATE:
        _BY_STATE[_state] = []
    _BY_STATE[_state].append(_loc)

# All state codes available
ALL_STATES = sorted(_BY_STATE.keys())


def get_locations_by_state(state: str) -> list:
    """Get all locations for a given state code."""
    return _BY_STATE.get(state.upper(), [])


def get_random_location(state: str | None = None) -> tuple:
    """Get a random location, optionally filtered by state."""
    import random
    if state:
        locs = get_locations_by_state(state)
        if not locs:
            locs = US_LOCATIONS
    else:
        locs = US_LOCATIONS
    return random.choice(locs)
